
let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);

// Increment (++)
console.log(++x);
console.log(x++);
console.log(x);

// Decrement (--)
console.log(--x);
console.log(x--);
console.log(x);

x = 10;

x = x - 5;
x += 5;

x = x * 5;
x /= 5;
